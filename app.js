const QUESTIONS = PRODUCT.freeQuestionBank;
const categoryColorMap = new Map(
  PRODUCT.categories.map((category) => [category.name, category.color])
);

const state = {
  order: [],
  currentIndex: 0,
  marked: new Set(),
  questionStartedAt: Date.now(),
  timerId: null
};

const $ = (selector) => document.querySelector(selector);

function buildQuestionOrder() {
  state.order = QUESTIONS.map((_, index) => index);

  if (PRODUCT.questionOrder === "sequential") {
    return;
  }

  for (let index = state.order.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    [state.order[index], state.order[swapIndex]] = [state.order[swapIndex], state.order[index]];
  }
}

function formatTime(ms) {
  const seconds = Math.floor(ms / 1000);
  const mins = String(Math.floor(seconds / 60)).padStart(2, "0");
  const secs = String(seconds % 60).padStart(2, "0");
  return `${mins}:${secs}`;
}

function currentQuestion() {
  const orderIndex = state.order[state.currentIndex];
  return QUESTIONS[orderIndex];
}

function getCategoryColor(label) {
  return categoryColorMap.get(label) || PRODUCT.accentColor;
}

function renderTimer() {
  const timerText = $("#timerText");
  if (!timerText) return;
  timerText.textContent = formatTime(Date.now() - state.questionStartedAt);
}

function renderDistribution() {
  const list = $("#distributionList");
  if (!list) return;

  const orderedNames = PRODUCT.categories.map((category) => category.name);
  const counts = QUESTIONS.reduce((distribution, question) => {
    distribution.set(question.primaryCategory, (distribution.get(question.primaryCategory) || 0) + 1);
    return distribution;
  }, new Map());

  const rows = orderedNames
    .filter((name) => counts.has(name))
    .map((name) => ({ name, count: counts.get(name), color: getCategoryColor(name) }));

  list.replaceChildren(...rows.map((entry) => {
    const row = document.createElement("div");
    row.className = "distribution-row";
    row.style.setProperty("--category-color", entry.color);
    row.innerHTML = `
      <span class="category-dot"></span>
      <span>${entry.name}</span>
      <strong>${entry.count}</strong>
    `;
    return row;
  }));
}

function renderMarkedList() {
  const markedCount = $("#markedCount");
  const markedList = $("#markedList");

  if (markedCount) {
    markedCount.textContent = String(state.marked.size);
  }

  if (!markedList) return;

  const markedQuestions = QUESTIONS.filter((question) => state.marked.has(question.id));
  if (markedQuestions.length === 0) {
    markedList.textContent = "No questions marked yet.";
    return;
  }

  markedList.replaceChildren(...markedQuestions.map((question) => {
    const item = document.createElement("div");
    item.className = "marked-item";
    item.innerHTML = `<strong>${question.id}</strong><span>${question.question}</span>`;
    return item;
  }));
}

function renderCompletion() {
  $("#progressText").textContent = `${QUESTIONS.length} / ${QUESTIONS.length}`;
  $("#progressFill").style.width = "100%";
  $("#questionNumber").textContent = "Complete";
  $("#questionId").textContent = "DONE";
  $("#questionText").textContent = PRODUCT.completionTitle;
  $("#questionNote").hidden = true;
  $("#tagList").replaceChildren();
  $("#answerAngleList").replaceChildren(...PRODUCT.completionSteps.map((text) => {
    const item = document.createElement("li");
    item.textContent = text;
    return item;
  }));
  $("#remainingText").textContent = "0 remaining";
  $("#nextButton").disabled = true;
  $("#reviewButton").disabled = true;
}

function buildTagChip(text, color) {
  const chip = document.createElement("span");
  chip.className = "tag-chip";
  chip.textContent = text;
  if (color) {
    chip.style.setProperty("--chip-color", color);
  }
  return chip;
}

function renderQuestion() {
  if (state.currentIndex >= QUESTIONS.length) {
    renderCompletion();
    renderMarkedList();
    return;
  }

  const question = currentQuestion();
  const progress = state.currentIndex + 1;
  const primaryColor = getCategoryColor(question.primaryCategory);

  $("#progressText").textContent = `${progress} / ${QUESTIONS.length}`;
  $("#progressFill").style.width = `${(progress / QUESTIONS.length) * 100}%`;
  $("#questionNumber").textContent = `Question ${progress}`;
  $("#questionId").textContent = question.id;
  $("#questionText").textContent = question.question;
  $("#remainingText").textContent = `${QUESTIONS.length - progress} remaining`;

  const note = $("#questionNote");
  if (question.note) {
    note.textContent = question.note;
    note.hidden = false;
  } else {
    note.hidden = true;
  }

  const chips = [buildTagChip(question.primaryCategory, primaryColor)];

  for (const category of question.categories || []) {
    chips.push(buildTagChip(category));
  }

  if (question.difficulty) {
    chips.push(buildTagChip(question.difficulty, PRODUCT.secondaryColor));
  }

  for (const tag of question.tags || []) {
    chips.push(buildTagChip(tag, PRODUCT.warmColor));
  }

  $("#tagList").replaceChildren(...chips);
  $("#answerAngleList").replaceChildren(...question.answerAngle.map((text) => {
    const item = document.createElement("li");
    item.textContent = text;
    return item;
  }));

  $("#reviewButton").textContent = state.marked.has(question.id) ? "Marked for Review" : "Mark for Review";
  $("#nextButton").disabled = false;
  $("#reviewButton").disabled = false;
  renderMarkedList();
}

function nextQuestion() {
  state.currentIndex += 1;
  state.questionStartedAt = Date.now();
  renderQuestion();
  renderTimer();
}

function toggleReview() {
  const question = currentQuestion();
  if (!question) return;

  if (state.marked.has(question.id)) {
    state.marked.delete(question.id);
  } else {
    state.marked.add(question.id);
  }

  renderQuestion();
}

function resetSession() {
  buildQuestionOrder();
  state.currentIndex = 0;
  state.marked.clear();
  state.questionStartedAt = Date.now();
  renderQuestion();
  renderTimer();
}

function initPracticePage() {
  $("#nextButton").addEventListener("click", nextQuestion);
  $("#reviewButton").addEventListener("click", toggleReview);
  $("#resetButton").addEventListener("click", resetSession);
  document.addEventListener("keydown", (event) => {
    if (event.key === "ArrowRight" && !$("#nextButton").disabled) nextQuestion();
    if (event.key.toLowerCase() === "m" && !$("#reviewButton").disabled) toggleReview();
  });
  renderDistribution();
  resetSession();
  state.timerId = window.setInterval(renderTimer, 1000);
}

if (document.body.dataset.page === "practice") {
  initPracticePage();
}
